// Função para substituir emojis por ícones
function substituirEmojis() {
  // Substituir emojis nos botões e elementos
  const elementos = document.querySelectorAll('[data-emoji]');
  elementos.forEach(el => {
    const emoji = el.dataset.emoji;
    let icone = '';
    
    switch(emoji) {
      case 'car': icone = criarIconeCarro(); break;
      case 'tools': icone = criarIconeFerramentas(); break;
      case 'bumper': icone = criarIconeParaChoque(); break;
      case 'spoiler': icone = criarIconeSpoiler(); break;
      case 'skirts': icone = criarIconeSaias(); break;
      case 'hood': icone = criarIconeCapo(); break;
      case 'material': icone = criarIconeMaterial(); break;
      case 'save': icone = criarIconeSalvar(); break;
      case 'export': icone = criarIconeExportar(); break;
      case 'history': icone = criarIconeHistorico(); break;
      case 'help': icone = criarIconeAjuda(); break;
      case 'rotate': icone = criarIconeRotacionar(); break;
      case 'zoom-in': icone = criarIconeZoomIn(); break;
      case 'zoom-out': icone = criarIconeZoomOut(); break;
      case 'reset': icone = criarIconeReset(); break;
      case '3d': icone = criarIcone3D(); break;
      case 'orcamento': icone = criarIconeOrcamento(); break;
    }
    
    if (icone) {
      // Se o elemento já tem conteúdo, adicionar antes do texto
      if (el.textContent && el.textContent.trim()) {
        el.innerHTML = icone + ' ' + el.textContent.trim();
      } else {
        el.innerHTML = icone;
      }
    }
  });
}

